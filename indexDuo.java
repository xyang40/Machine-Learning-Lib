public class indexDuo {

    private int firstdimensionindex;
    private int seconddimensionindex;

    public indexDuo(int f, int s) {
        this.firstdimensionindex = f;
        this.seconddimensionindex = s;
    }

    public int getFirstDimensionIndex() {
        return this.firstdimensionindex;
    }

    public int getSecondDimensionIndex() {
        return this.seconddimensionindex;
    }
}
